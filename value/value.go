// This Example for value

package main

import "fmt"

func main() {
  fmt.Println("GOLANG "+" VALUE") // this value string

  fmt.Println("1 + 1 = ", 1+1) // this value number

  fmt.Println("7.0 / 3.0", 7.0/3.0) // this decimal

  fmt.Println(true && true)

  fmt.Println(false && true)

  fmt.Println(false || true)

  fmt.Println(true || false)

}
